import pandas as pd
import numpy as np
import seaborn as sns
import matplotlib.pyplot as plt

df3 = pd.read_csv('df3.csv')

#-------------------------------------------------------------------------------
#   Excercise 1: Scatter plot
#-------------------------------------------------------------------------------

scat = df3.plot.scatter(x = 'a', y = 'b', c = 'red', s = 50, figsize = (12, 3))
plt.xlim(-0.2, 1.2)
plt.ylim(-0.2, 1.2)
plt.show(scat)


#-------------------------------------------------------------------------------
#   Excercise 2: hist plot
#-------------------------------------------------------------------------------

hist = df3['a'].plot.hist(bins = 10)
plt.show(hist)

#-------------------------------------------------------------------------------
#   Excercise 3: referbish hist plot
#-------------------------------------------------------------------------------

plt.style.use('ggplot')
hist2 = df3['a'].plot.hist(bins = 25, alpha = 0.6)
plt.show(hist2)

#-------------------------------------------------------------------------------
#   Excercise 4: box plot
#-------------------------------------------------------------------------------

box = df3[['a', 'b']].plot.box()
plt.show(box)

#-------------------------------------------------------------------------------
#   Excercise 5: KDE plot (solid line)
#-------------------------------------------------------------------------------

kde1 = df3['d'].plot.kde(c = 'red')
plt.show(kde1)

#-------------------------------------------------------------------------------
#   Excercise 6: KDE plot (dashed thick line)
#-------------------------------------------------------------------------------

kde2 = df3['d'].plot.kde(c = 'red', lw = 5, ls = '--')
plt.show(kde2)

#-------------------------------------------------------------------------------
#   Excercise 7: area plot index 0 to 30, use .ix[0:30]
#-------------------------------------------------------------------------------

area1 = df3.ix[0:30].plot.area(alpha = 0.4)
plt.show(area1)

#-------------------------------------------------------------------------------
#   Excercise 8: area plot, move legend off of the graph
#-------------------------------------------------------------------------------
fig = plt.figure()
area2 = df3.ix[0:30].plot.area(alpha = 0.4, ax = fig.gca())
plt.legend(loc = 'center left', bbox_to_anchor = (1.0, 0.5))
plt.show(area2)
